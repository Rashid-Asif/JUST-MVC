       <p>Model-View-Controller:MVC is a software architecture, or design pattern, that is used in software engineering, whose fundamental principle is based on the idea that the logic of an application should be separated from its presentation. Put simply, I would say that MVC is simply a better way of separating the logic of your application from the display.</p>

        <strong class="page-header">A simple way to think of this would be to consider the following:</strong><br />
        ->A user interacts with the view - by clicking on a link or submitting a form.<br />
        ->The Controller handles the user input, and transfers the information to the model<br />
        ->The Model receives the information and updates it's state (adds data to a database, for example, or calculates todays date)<br />
        ->The View checks the state of the Model and responds accordingly (listing the newly entered data, maybe)<br />
        ->The View waits for another interaction from the user.
